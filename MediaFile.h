#ifndef MEDIAFILE_H  // Если MEDIAFILE_H не определён, то определить его (защита от повторного включения)
#define MEDIAFILE_H  // Определяем MEDIAFILE_H, чтобы предотвратить повторное включение заголовочного файла

#include <iostream>  // Подключаем библиотеку для работы с вводом/выводом (cout, cin)
#include <string>    // Подключаем библиотеку для работы со строками (std::string)
using namespace std; // Используем пространство имён std, чтобы не писать std:: перед string и cout

/**
 * @brief Базовый класс для всех медиафайлов.
 *
 * Этот класс содержит общие свойства для видео- и аудиофайлов,
 * такие как название и длительность.
 */
class MediaFile {
public:
    string name;     ///< Название файла
    int duration;    ///< Длительность файла в секундах

    /**
     * @brief Конструктор класса MediaFile.
     * @param n Название файла
     * @param d Длительность файла в секундах
     *
     * Создаёт объект MediaFile и инициализирует его название и длительность.
     */
    MediaFile(string n, int d) : name(n), duration(d) {}

    /**
     * @brief Выводит информацию о файле.
     *
     * В базовом классе этот метод просто выводит название и длительность.
     * В производных классах его можно переопределить, добавляя дополнительные данные.
     */
    virtual void info() {
        cout << "Файл: " << name << ", Длительность: " << duration << " сек" << endl;
    }
};

#endif // Завершаем условную компиляцию (если файл уже был включён, он не подключится снова)
